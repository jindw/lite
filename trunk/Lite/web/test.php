function html2xml($s) {
	global $namedentities, $emptytags, $autoclosetags, $autoclosekeys;
	$r2 = "";
	$r = "";
	$limit = strlen($s);
	$state = states_text;
	$prevstate = $state;
	$opentags = array();
	$name = "";
	$tagname = "";
	$attrname = "";
	$attrs = "";
	$attrnames = array();
	$entvalue = 0;
	$attrdelim = "\"";
	$attrvalue = "";
	$cs = "";
	$prec = " ";
	$preprec = " ";
	$c = " ";
	if(substr($s, 0, 3) == "\xef\xbb\xbf") {
		$encoding = "utf-8";
		$start = 3;
	} else {
		$encoding = "iso-8859-1";
		$start = 0;
	}
	for($i=$start; $i<$limit && (($r2=="" && $r=="") || count($opentags)!=0); $i++) {
		if(strlen($r) > 10240) {
			$r2 .= $r;
			$r = "";
		}
		$c = $s{$i};
		switch($state) {
			case states_text:
				if($c == "<") {
					$name = "";
					$tagname = "";
					$attrname = "";
					$attrs = "";
					$attrnames = array();
					$state = states_tag;
					break;
				}
				if(!ctype_space($c) && count($opentags)==0) {
					$r .= "<html>";
					array_push($opentags, "html");
				}
				if(ctype_space($c) && count($opentags)==0) {
					break;
				}
				if($c == "&") {
					$name = "";
					$entvalue = 0;
					$prevstate = $state;
					$state = states_entity;
					break;
				}
				$r .= $c;
				break;
			case states_tag:
				if($c=="?" && $tagname=="") {
					$state = states_tillinst;
					break;
				}
				if($c=="!" && $tagname=="") {
					$state = states_specialtag;
					$prec = " ";
					break;
				}
				if($c=="/" && $name=="" && $tagname=="") {
					$state = states_endtag;
					$name = "";
					break;
				}
				if(ctype_space($c)) {
					if($name=="") {
						break;
					}
					if($tagname=="" && $name!="_") {
						$tagname = $name;
						$name = "";
						break;
					}
					if($attrname=="" && $name!="_") {
						$attrname = strtolower($name);
						$name = "";
						break;
					}
					break;
				}
				if($c=="=") {
					if($attrname=="" && $name!="_") {
						$attrname = strtolower($name);
						$name = "";
					}
					$state = states_tillquote;
					break;
				}
				if($c=="/" && ($tagname!="" || $name!="")) {
					if($tagname=="") {
						$tagname = $name;
					}
					$tagname = strtolower($tagname);
					if($tagname!="html" && count($opentags)==0) {
						$r .= "<html>";
						array_push($opentags, "html");
					}
					if(in_array($tagname, $autoclosekeys) && count($opentags) > 0) {
						$prevtag = $opentags[count($opentags)-1];
						if(in_array($prevtag, $autoclosetags[$tagname])) {
							array_pop($opentags);
							$r .= "</".$prevtag.">";
						}
					}
					$r .= "<".$tagname.$attrs."/>";
					$state = states_tillgt;
					break;
				}
				if($c==">") {
					if($tagname=="" && $name!="") {
						$tagname = $name;
					}
					if($tagname!="") {
						$tagname = strtolower($tagname);
						if($tagname!="html" && count($opentags)==0) {
							$r .= "<html>";
							array_push($opentags, "html");
						}
						if(in_array($tagname, $autoclosekeys) && count($opentags) > 0) {
							$prevtag = $opentags[count($opentags)-1];
							if(in_array($prevtag, $autoclosetags[$tagname])) {
								array_pop($opentags);
								$r .= "</".$prevtag.">";
							}
						}
						if(in_array($tagname, $emptytags)) {
							$r .= "<".$tagname.$attrs."/>";
						} else {
							array_push($opentags, $tagname);
							$r .= "<".$tagname.$attrs.">";
							if($tagname=="script") {
								$r .= "<![CDATA[";
								array_pop($opentags);
								$state = states_script;
								break;
							}
						}
						$state = states_text;
						break;
					}
				}
				if($attrname!="") {
					$attrs .= " ".$attrname."=\"".$attrname."\"";
					$attrname = "";
				}
				$name .= (ctype_alnum($c) && $name!="") || ctype_alpha($c) ? $c : ($name=="" ? "_" : ($c=="-" ? "-" : ($name != "_" ? "_" : "")));
				break;
			case states_endtag:
				if($c==">") {
					$name = strtolower($name);
					if(in_array($name, $opentags)) {
						while(($prevtag = array_pop($opentags)) != $name) {
							$r .= "</".$prevtag.">";
						}
						$r .= "</".$name.">";
					} else {
						if($name!="html" && count($opentags)==0) {
							$r .= "<html>";
							array_push($opentags, "html");
						}
					}
					$state = states_text;
					break;
				}
				if(ctype_space($c)) {
					break;
				}
				$name .= ctype_alnum($c) ? $c : ($name != "_" ? "_" : "");
				break;
			case states_attrtext:
				if($c==$attrdelim || (ctype_space($c) && $attrdelim==" ")) {
					if(!in_array($attrname, $attrnames)) {
						array_push($attrnames, $attrname);
						$attrs .= " ".$attrname."=\"".$attrvalue."\"";
					}
					$attrname = "";
					$state = states_tag;
					break;
				}
				if($attrdelim==" " && ($c=="/" || $c==">")) {
					$tagname = strtolower($tagname);
					if($tagname!="html" && count($opentags)==0) {
						$r .= "<html>";
						array_push($opentags, "html");
					}
					if(in_array($tagname, $autoclosekeys) && count($opentags) > 0) {
						$prevtag = $opentags[count($opentags)-1];
						if(in_array($prevtag, $autoclosetags[$tagname])) {
							array_pop($opentags);
							$r .= "</".$prevtag.">";
						}
					}
					if(!in_array($attrname, $attrnames)) {
						array_push($attrnames, $attrname);
						$attrs .= " ".$attrname."=\"".$attrvalue."\"";
					}
					$attrname = "";
					if($c=="/") {
						$r .= "<".$tagname.$attrs."/>";
						$state = states_tillgt;
						break;
					}
					if($c==">") {
						if(in_array($tagname, $emptytags)) {
							$r .= "<".$tagname.$attrs."/>";
							$state = states_text;
							break;
						} else {
							array_push($opentags, $tagname);
							$r .= "<".$tagname.$attrs.">";
							if($tagname=="script") {
								$r .= "<![CDATA[";
								array_pop($opentags);
								$prec = " ";
								$preprec = " ";
								$state = states_script;
								break;
							}
							$state = states_text;
							break;
						}
					}
				}
				if($c=="&") {
					$name = "";
					$entvalue = 0;
					$prevstate = $state;
					$state = states_entity;
					break;
				}
				$attrvalue .= $c=="\"" ? "&quot;" : ($c == "'" ? "&apos;" : $c);
				break;
			case states_script:
				if($c=="/" && $prec=="<") {
					$state = states_endscript;
					$name = "";
					break;
				}
				if($c=="[" && $prec=="!" && $preprec=="<") {
					$state = states_skipcdata;
					$name = "<![";
					break;
				}
				if($c==">" && $prec=="]" && $preprec=="]") {
					$c = $r{strlen($r)-3};
					$r = substr($r, 0, strlen($r)-4);
				}
				$r .= $c;
				$preprec = $prec;
				$prec = $c;
				break;
			case states_endscript:
				if($c==">" && strtolower($name)=="script") {
					$r = substr($r, 0, strlen($r)-1);
					$r .= "]]></script>";
					$state = states_text;
					break;
				}
				$name .= $c;
				if(substr("script", 0, strlen($name)) != strtolower($name)) {
					$r .= $name;
					$state = states_script;
				}
				break;
			case states_specialtag:
				if($c!="-") {
					$state = states_tillgt;
					break;
				}
				if($prec=="-") {
					$state = states_comment;
					$preprec = " ";
					break;
				}
				$prec = $c;
				break;
			case states_comment:
				if($c==">" && $prec=="-" && $preprec=="-") {
					$state = states_text;
					break;
				}
				$preprec = $prec;
				$prec = $c;
				break;
			case states_skipcdata:
				if($name=="<![CDATA[") {
					$state = states_script;
					break;
				}
				$name .= $c;
				if(substr("<![CDATA[", 0, strlen($name)) != $name) {
					$r .= $name;
					$state = states_script;
				}
				break;
			case states_entity:
				if($c=="#") {
					$state = states_numericentity;
					break;
				}
				$name .= $c;
				$state = states_namedentity;
				break;
			case states_numericentity:
				if($c=="x" || $c=="X") {
					$state = states_hexaentity;
					break;
				}
				if($c==";") {
					$ent = "&#".$entvalue.";";
					if($prevstate==states_text) {
						$r .= $ent;
					} else {
						$attrvalue .= $ent;
					}
					$state = $prevstate;
					break;
				}
				$entvalue = $entvalue * 10 + ord($c) - ord("0");
				break;
			case states_hexaentity:
				if($c==";") {
					$ent = "&#".$entvalue.";";
					if($prevstate==states_text) {
						$r .= $ent;
					} else {
						$attrvalue .= $ent;
					}
					$state = $prevstate;
					break;
				}
				$entvalue = $entvalue * 16 + (ctype_digit($c) ? ord($c)-ord("0") : ord(strtoupper($c)) - ord("A"));
				break;
			case states_namedentity:
				if($c==";") {
					$name = strtolower($name);
					if($name=="amp" || $name=="lt" || $name=="gt" || $name=="quot" || $name=="apos") {
						$ent = "&".$name.";";
						$name = "";
						if($prevstate==states_text) {
							$r .= $ent;
						} else {
							$attrvalue .= $ent;
						}
						$state = $prevstate;
						break;
					}
					if(in_array($name, array_keys($namedentities))) {
						$entvalue = $namedentities[$name];
					}
					$ent = "&#".$entvalue.";";
					$name = "";
					if($prevstate==states_text) {
						$r .= $ent;
					} else {
						$attrvalue .= $ent;
					}
					$state = $prevstate;
					break;
				}
				if(!ctype_alnum($c) || strlen($name) > 6) {
					$ent = "&amp;".$name;
					$name = "";
					if($prevstate==states_text) {
						$r .= $ent;
					} else {
						$attrvalue .= $ent;
					}
					$state = $prevstate;
					$i--;
					break;
				}
				$name .= $c;
				break;
			case states_tillinst:
				if($c=="?") {
					$state = states_andgt;
				}
				break;
			case states_andgt:
				if($c==">") {
					$state = states_text;
					break;
				}
				$state = states_tillinst;
				break;
			case states_tillgt:
				if($c==">") {
					$state = states_text;
				}
				break;
			case states_tillquote:
				if(ctype_space($c)) {
					break;
				}
				if($c=="\"" || $c=="'") {
					$attrdelim = $c;
					$attrvalue = "";
					$state = states_attrtext;
					break;
				}
				if($c=="/" || $c==">") {
					$attrs .= " ".$attrname."=\"".$attrname."\"";
				}
				if($c=="/") {
					$r .= "<".strtolower($tagname).$attrs."/>";
					$state = states_tillgt;
					break;
				}
				if($c==">") {
					$tagname = $tagname.ToLower();
					if($tagname!="html" && count($opentags)==0) {
						$r .= "<html>";
						array_push($opentags, "html");
					}
					if(in_array($tagname, $autoclosekeys) && count($opentags) > 0) {
						$prevtag = $opentags[count($opentags)-1];
						if(in_array($prevtag, $autoclosetags[$tagname])) {
							array_pop($opentags);
							$r .= "</".$prevtag.">";
						}
					}
					if(in_array($tagname, $emptytags)) {
						$r .= "<".$tagname.$attrs."/>";
						$state = states_text;
						break;
					} else {
						array_push($opentags, $tagname);
						$r .= "<".$tagname.$attrs.">";
						if($tagname=="script") {
							$r .= "<![CDATA[";
							array_pop($opentags);
							$state = states_script;
							break;
						}
					}
				}
				$attrdelim = " ";
				$attrvalue = $c;
				$state = states_attrtext;
				break;
		}
	}
	while(count($opentags) != 0) {
		$r .= "</".array_pop($opentags).">";
	}
	$r2 .= $r;
	return "<?xml version=\"1.0\" encoding=\"".$encoding."\"?>\n".$r2;
}
